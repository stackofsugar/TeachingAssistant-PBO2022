public class DeklarasiVariabel {
    public static void main(String[] args) {
        // Mendeklarasikan 1 variabel
        int a = 4;
        // Mendeklarasikan banyak variabel
        float b = 2.6f, c = 4.5f, d = 8.9f;
        // Mendefinisikan beberapa variabel
        // dengan satu value
        String e, f, g;
        e = f = g = "Hello";

        // ...
        System.out.println(a); // 4
        System.out.println(b); // 2.6
        System.out.println(g); // Hello
        // ...
    }
}
